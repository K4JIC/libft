#include <stdio.h>

int	ft_isdigit(int c)
{
	if (c == EOF)
		return (0);
	if (('a' <= c && c <= 'f')
		|| ('0' <= c && c <= '9')
		|| ('A' <= c && c <= 'Z'))
		return (1);
	return (0);
}
